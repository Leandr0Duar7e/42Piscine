#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	tab2[2];
	int	i;

	i = 0;
	while (i < size / 2)
	{
		tab2[0] = *(tab + i);
		tab2[1] = *(tab + size - i - 1);
		*(tab + i) = tab2[1];
		*(tab + size - i - 1) = tab2[0];
		i++;
	}
}
